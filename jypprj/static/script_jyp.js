function idverify(){
	$.ajax({
			url : "idconfirm",
			type : "POST",
			data : {
				"id" : $("input[name=id]").val()
			},
			dataType : "text",
			success : function (data) {
				if (data =="중복되는 아이디 입니다."){
					registerform.id_check.value=0				
				} else if (data =="사용가능한 아이디입니다."){
					registerform.id_check.value=1	
				} else if(data=="영어와 숫자만 입력해주세요."){
					registerform.id_check.value=0
				}
				
				$("#id_result").html(data);
			},
			error : function () {}
		}
	);
}

