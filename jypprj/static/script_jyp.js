var eng = /^[a-zA-Z]*$/; 
function idconfirm() {
	
	if( ! $("input[name=id]").val() ) {
		alert( "아이디를 입력하세요" );
		$("input[name=id]").focus();
		return false; 		
	} else if(  ! eng.test( $( "input[name=id]" ).val() ) ){
			alert("아이디는 영어만 사용해주세요.");
			$("input[name=id]").focus();
			return false;
	}else {
		url = "idconfirm" + "?id=" + $("input[name=id]").val();
		open( url, "confirm", 
		"toolbar=no, menubar=no, scrollbar=no, status=no, width=400, height=250" );
	}
}
